public class SwitchCharExample {
    public static void main(String[] args) {
        char grade = 'a';

        switch(grade) {
            case 'A':
            case'a':
                System.out.println("아이고, 우수 회원님~~~ 안녕하세요. 어서 오십시오.");
                break;
            case'B':
            case'b':
                System.out.println("회원님 어서오세요. ");
                break;
            default:
                System.out.println("어... 왔니?");





        }
    }
}
