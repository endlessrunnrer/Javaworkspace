package chap_02;

public class _02_operator22 {
    public static void main(String[] args) {

        int num = 10;
        num = num + 2;
        System.out.println(num);

        num = num % 3;
        System.out.println(num);


        num += 2;
        System.out.println(num);
        // num = num + 2;

        num -= 2;
        System.out.println(num);

    }
}
